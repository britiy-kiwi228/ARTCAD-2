#ifndef ULTRASONIC_H
#define ULTRASONIC_H

#include <stdint.h>

/**
 * Структура для хранения данных ультразвукового датчика HC-SR04
 * 
 * Поля:
 *   - trig_pin: номер пина GPIO для TRIGGER (запуск импульса)
 *   - echo_pin: номер пина GPIO для ECHO (прием сигнала)
 *   - last_distance_cm: последнее измеренное расстояние в сантиметрах (float)
 *   - pulse_start_us: время начала импульса в микросекундах (для ISR)
 */
typedef struct {
    uint8_t trig_pin;
    uint8_t echo_pin;
    float last_distance_cm;
    uint32_t pulse_start_us;
} Ultrasonic_t;

/**
 * Инициализация датчика расстояния HC-SR04
 * 
 * Функция:
 *   - Настривает режимы пинов (OUTPUT для TRIGGER, INPUT для ECHO)
 *   - Подключает прерывание на пин ECHO с режимом CHANGE (отлавливает rising и falling edges)
 *   - Инициализирует начальный отсчет расстояния
 * 
 * Параметры:
 *   - sensor: указатель на структуру Ultrasonic_t
 * 
 * Возвращает: ничего (void)
 */
void ultrasonic_init(Ultrasonic_t *sensor);

/**
 * Запуск одного измерения расстояния
 * 
 * Функция:
 *   - Отправляет короткий импульс (10 микросекунд) на пин TRIGGER
 *   - Эта функция должна вызываться с частотой ~10 раз в секунду (каждые 100 мс)
 *   - Прерывание на пине ECHO автоматически обработает сигнал и рассчитает расстояние
 * 
 * Параметры:
 *   - sensor: указатель на структуру Ultrasonic_t
 * 
 * Возвращает: ничего (void)
 */
void ultrasonic_start_measurement(Ultrasonic_t *sensor);

/**
 * Получить последнее измеренное расстояние в сантиметрах
 * 
 * Функция:
 *   - Возвращает результат последнего выполненного измерения
 *   - Безопасно читает глобальные переменные (с отключением прерываний)
 *   - Если таймаут (расстояние > 400 см или датчик не отозвался), вернет -1.0
 * 
 * Параметры:
 *   - sensor: указатель на структуру Ultrasonic_t
 * 
 * Возвращает: расстояние в float (сантиметры) или -1.0 при таймауте
 */
float ultrasonic_get_distance_cm(Ultrasonic_t *sensor);

#endif // ULTRASONIC_H
